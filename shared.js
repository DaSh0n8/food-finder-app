// Constructing Classes

class SearchResult {
    constructor(name, lat, lng, address, category, bookmarked, review){
        this.name = name;
        this.lat = lat;
        this.lng = lng;
        this.address = address;
        this.category = category;
        this.bookmarked = bookmarked = false;
        this.review = review; 
        
    }
}